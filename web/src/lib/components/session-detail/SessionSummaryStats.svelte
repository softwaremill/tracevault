					</Tooltip.Portal>
				</Tooltip.Root>
			</div>
		</Card.Root>

		<Card.Root class="p-3">
			<div class="text-muted-foreground text-xs uppercase tracking-wide">
				Compactions
				<Tooltip.Root>
					<Tooltip.Trigger>
						<span class="ml-1 cursor-help text-xs" style="color: #4f6ef7">?</span>
					</Tooltip.Trigger>
					<Tooltip.Portal>
						<Tooltip.Content class="max-w-xs">
							Number of times the context window was compacted to fit within limits.
						</Tooltip.Content>
					</Tooltip.Portal>
				</Tooltip.Root>
			</div>
			<div class="mt-1 text-xl font-semibold">{compactions}</div>
		</Card.Root>
	</div>

