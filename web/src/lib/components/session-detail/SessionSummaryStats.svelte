					</Tooltip.Trigger>
					<Tooltip.Portal>
						<Tooltip.Content class="max-w-xs">
							Total tokens processed in this session across all API calls.
						</Tooltip.Content>
					</Tooltip.Portal>
				</Tooltip.Root>
			</div>
			<div class="mt-1 text-xl font-semibold">{fmtNum(totalTokens)}</div>
		</Card.Root>

		<Card.Root class="p-3">
			<div class="text-muted-foreground text-xs uppercase tracking-wide">
				Total Cost
				<Tooltip.Root>
					<Tooltip.Trigger>
						<span class="ml-1 cursor-help text-xs" style="color: #4f6ef7">?</span>
					</Tooltip.Trigger>
					<Tooltip.Portal>
						<Tooltip.Content class="max-w-xs">
							Estimated session cost based on token usage and model pricing.
						</Tooltip.Content>
					</Tooltip.Portal>
				</Tooltip.Root>
			</div>
			<div class="mt-1 text-xl font-semibold" style="color: #3ecf8e">{fmtCost(estimatedCostUsd)}</div>
		</Card.Root>

		<Card.Root class="p-3">
			<div class="text-muted-foreground text-xs uppercase tracking-wide">
				Output Tokens
				<Tooltip.Root>
					<Tooltip.Trigger>
						<span class="ml-1 cursor-help text-xs" style="color: #4f6ef7">?</span>
					</Tooltip.Trigger>
					<Tooltip.Portal>
						<Tooltip.Content class="max-w-xs">
							Tokens generated by the AI model in this session.
						</Tooltip.Content>
					</Tooltip.Portal>
				</Tooltip.Root>
			</div>
			<div class="mt-1 text-xl font-semibold" style="color: #a78bfa">{fmtNum(outputTokens)}</div>
		</Card.Root>

		<Card.Root class="p-3">
			<div class="text-muted-foreground text-xs uppercase tracking-wide">
				API Calls
				<Tooltip.Root>
					<Tooltip.Trigger>
						<span class="ml-1 cursor-help text-xs" style="color: #4f6ef7">?</span>
					</Tooltip.Trigger>
					<Tooltip.Portal>
						<Tooltip.Content class="max-w-xs">
							Number of API roundtrips (assistant responses) in this session.
						</Tooltip.Content>
					</Tooltip.Portal>
				</Tooltip.Root>
			</div>
			<div class="mt-1 text-xl font-semibold">{apiCalls}</div>
		</Card.Root>

		<Card.Root class="p-3">
			<div class="text-muted-foreground text-xs uppercase tracking-wide">
				Cache Saved
				<Tooltip.Root>
					<Tooltip.Trigger>
						<span class="ml-1 cursor-help text-xs" style="color: #4f6ef7">?</span>
					</Tooltip.Trigger>
					<Tooltip.Portal>
						<Tooltip.Content class="max-w-xs">
							Percentage of input-side tokens served from cache instead of being freshly processed. Higher is better.
						</Tooltip.Content>
					</Tooltip.Portal>
				</Tooltip.Root>
			</div>
			<div class="mt-1 text-xl font-semibold" style="color: #3ecf8e">
				{cacheSavings.cache_hit_percentage.toFixed(1)}%
			</div>
			<div class="text-muted-foreground mt-0.5 text-xs">
				<Tooltip.Root>
					<Tooltip.Trigger>
						<span class="cursor-help">-{fmtCost(cacheSavings.gross_savings_usd)} gross</span>
					</Tooltip.Trigger>
					<Tooltip.Portal>
						<Tooltip.Content class="max-w-xs">
							Amount saved by serving tokens from cache at the reduced cache-read rate instead of the full input rate.
						</Tooltip.Content>
					</Tooltip.Portal>
				</Tooltip.Root>
				{' · '}
				<Tooltip.Root>
					<Tooltip.Trigger>
						<span class="cursor-help">-{fmtCost(cacheSavings.net_savings_usd)} net</span>
					</Tooltip.Trigger>
					<Tooltip.Portal>
						<Tooltip.Content class="max-w-xs">
							Gross savings minus the overhead of writing tokens to cache, which costs more than base input. This is your true savings.
